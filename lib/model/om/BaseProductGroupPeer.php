			$con = Propel::getConnection(self::DATABASE_NAME);
		}

		$objs = null;
		if (empty($pks)) {
			$objs = array();
		} else {
			$criteria = new Criteria();
			$criteria->add(ProductGroupPeer::ID, $pks, Criteria::IN);
			$objs = ProductGroupPeer::doSelect($criteria, $con);
		}
		return $objs;
	}

