			$con = Propel::getConnection(ProductGroupPeer::DATABASE_NAME);
		}

		if ($values instanceof Criteria) {
			$criteria = clone $values; 		} elseif ($values instanceof ProductGroup) {

			$criteria = $values->buildPkeyCriteria();
