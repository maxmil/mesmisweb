	private static $fieldKeys = array (
		BasePeer::TYPE_PHPNAME => array ('Id' => 0, 'Alias' => 1, ),
		BasePeer::TYPE_COLNAME => array (ProductGroupPeer::ID => 0, ProductGroupPeer::ALIAS => 1, ),
		BasePeer::TYPE_FIELDNAME => array ('id' => 0, 'alias' => 1, ),
