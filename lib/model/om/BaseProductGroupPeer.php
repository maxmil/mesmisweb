	private static $phpNameMap = null;


	
	private static $fieldNames = array (
		BasePeer::TYPE_PHPNAME => array ('Id', 'Alias', ),
		BasePeer::TYPE_COLNAME => array (ProductGroupPeer::ID, ProductGroupPeer::ALIAS, ),
		BasePeer::TYPE_FIELDNAME => array ('id', 'alias', ),
