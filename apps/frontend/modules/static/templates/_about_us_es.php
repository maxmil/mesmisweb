    <h1>EL PROYECTO <strong>MESMIS</strong></h1>
    <div class="separator"></div>
    <div class="section">
      <a name="1.1"></a>
      <h2>¿Qué es el Proyecto MESMIS?</h2>
      <p>El proyecto MESMIS es un esfuerzo interdisciplinario y multi-institucional dirigido a:</p>
      <ul>
        <li>El desarrollo de un marco metodológico con el fin de evaluar la sustentabilidad de sistemas alternativos de manejo de recursos naturales.</li>
        <li>La aplicación del marco en  una serie de estudios de caso en el sector rural.</li>
        <li>La generación y difusión de materiales didácticos y de referencia para facilitar la aplicación del marco MESMIS y la discusión sobre sustentabilidad.</li>
        <li>La capacitación de individuos y organizaciones interesadas en evaluar la sustentabilidad de los sistemas productivos a través de cursos, talleres y seminarios.</li>
        <li>Formar recursos humanos a través de programas universitarios de licenciatura y postgrado.</li>
      </ul>
      <p>El proyecto se inició en 1994 como parte de la Red de Gestión de Recursos Naturales, un programa  financiado por la Fundación Rockefeller en México. El proyecto MESMIS ha sido coordinado por GIRA A.C. desde sus inicios, integrando a un amplio grupo de instituciones académicas, gubernamentales y ONG.</p>
      <p>Los esfuerzos se han concentrado en el  desarrollo del marco de evaluación MESMIS  y su aplicación en diferentes estudios de caso, en los que se han evaluado diversos sistemas de manejo de recursos naturales tipo en México y Latinoamérica, poniendo énfasis en proyectos dirigidos a mejorar las condiciones del manejo de recursos naturales en el contexto campesino. Los equipos de evaluación han incluido personas de instituciones de investigación y ONG’s que trabajan directamente con comunidades y con diferentes niveles de participación campesina. En 1999 se publicó el marco MESMIS en forma de libro1 y actualmente se está trabajando en una segunda edición que integra los avances teóricos más importantes en la discusión de sustentabilidad a nivel internacional, así como la experiencia generada durante seis años de aplicación en diversos estudios de caso.</p>
    </div>
    <div class="section">
      <a name="1.2"></a>
      <h2>Áreas de trabajo</h2>
      <p>Una de las principales preocupaciones es la sistematización de las experiencias que se han generado a partir de la aplicación del marco MESMIS. En la actualidad se han documentado 11 estudios de caso en dos publicaciones que analizan la sustentabilidad de diferentes sistemas campesinos tipo de México y Latinoamérica.</p>
      <p>Adicionalmente se han generado diversos materiales didácticos, que incluyen:</p>
      <p>Un conjunto de manuales en donde se examinan los aspectos de mayor relevancia para la aplicación del marco de evaluación. Por ejemplo: la conformación del equipo interdisciplinario de evaluación, estrategias para la obtención de información, la integración y presentación de los resultados y otros aspectos.</p>
      <p>La base de datos SUSTENTA, la cual incluye más de 500 referencias nacionales e internacionales referentes al tema de sustentabilidad y su evaluación.</p>
      <p>Los diferentes materiales forman parte de un “paquete de evaluación” diseñado para proporcionar las herramientas teóricas y prácticas a individuos u organizaciones que deseen llevar a cabo una evaluación. Así mismo se está preparando un conjunto de cursos y seminarios con el fin de difundir el marco y los diferentes materiales que conforman el paquete de evaluación.</p>
