      <h2>Quienes somos</h2>
      <p>Uno de los objetivos del proyecto MESMIS ha sido el de consolidar un grupo de investigación, interinstitucional e interdisciplinario, en donde confluyen instituciones y organizaciones involucradas con el manejo de recursos naturales. Dicho grupo está  conformado por tres instituciones universitarias y una organización civil: Centro de Investigaciones en Ecosistemas (CIECO–UNAM), Centro de Investigaciones en Ciencias Agropecuarias (CICA–UAEM),  El Colegio de la Frontera Sur (ECOSUR) y el Grupo Interdisciplinario de Tecnología Rural Apropiada (GIRA A.C). Cada una de estas instituciones trabaja en la investigación de los temas emergentes relacionados con la evaluación de sustentabilidad tales como sistemas dinámicos, caracterización de sistemas de manejo de recursos naturales, indicadores de sustentabilidad, análisis multicriterio, entre otros. Así mismo, genera estudios de caso en diferentes regiones del país, que permiten validar la metodología y enriquecerla mediante la retroalimentación de la experiencia práctica. Los investigadores que conforman el grupo y sus líneas de investigación son las siguientes:</p>
      <p>Dra. Marta Astier, quien coordina el Programa de Agroecología en GIRA A.C.  Ha trabajado y publicado artículos sobre el tema de agricultura ecológica en particular el área de calidad de suelos y sistemas productivos de maíz. Dentro del grupo de investigación, sus temas de trabajo son: la sustentabilidad de los sistemas productivos campesinos, indicadores agronómicos y relaciones sinérgicas entre indicadores (“trade offs”) y sistematización de estudios de caso que utilizan el marco MESMIS.</p>
      <p>Dr. Omar Masera, es investigador titular del CIECO – UNAM y asesor de GIRA A.C. Sus líneas de trabajo son: energía y recursos naturales, cambio climático global y sustentabilidad. En esta última, sus esfuerzos se han enfocado a desarrollar las bases teóricas del concepto y su aplicación en sistemas de manejo de recursos naturales, haciendo énfasis en la integración de las dimensiones sociales y naturales. Es coautor del MESMIS y coeditor del libro que compila los primeros estudios de caso en México.</p>
      <p>Dr. Carlos González Esquivel, es investigador en el CICA – UAEM. El ha desarrollado una línea de investigación enfocada a la sustentabilidad de los sistemas de producción pecuarios. Su principal objetivo en el grupo, es el de generar herramientas teóricas y prácticas en el ámbito de los indicadores pecuarios y desarrollar estudios de caso para la medición y monitoreo de estos indicadores.</p>
      <p>Dr. Luis García Barrios es investigador del Colegio de la Frontera Sur (ECOSUR) en su sede San Cristóbal de las Casas. Su línea de investigación esta enfocada a desarrollar y analizar las bases teóricas de los sistemas complejos, sus propiedades o atributos y su aplicación en la evaluación de sustentabilidad. Trabaja principalmente con modelos y diseños experimentales basados en diferentes cultivos y asociaciones.</p>
      <p>M. en C. Santiago López-Ridaura es coautor del MESMIS y coeditor del libro que compila los estudios de caso en México. Actualmente, se encuentra realizando su estudios de doctorado en Wageningean, Holanda. Sus objetivos de investigación son el desarrollo de un marco multiescalar de evaluación de sustentabilidad y el análisis de “Trade offs”. Colabora permanentemente como asesor en GIRA A.C., en investigación y en el desarrollo del estudio de caso de la región Purépecha.</p>
