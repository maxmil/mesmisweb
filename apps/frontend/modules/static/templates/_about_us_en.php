corn. Inside the research team, her work topics are sustainability on productive farming systems, agronomic 
indicators, synergic relations, and trade offs systemization of case studies that use the MESMIS framework. </p>
      <h3>Doctor Omar Masera</h3>
      <p>Masera is a permanent researcher for the Center of Ecosystems Research (CIECO-UNAM) and is the GIRA 
A.C.’s assessor. His interests include natural resources and energy, sustainability and global weather change, in 
which his efforts have been focused in developing the theoretical conceptual bases and their application in 
management systems of natural resources, giving emphasis to the integration of social and natural dimensions. </p>
      <h3>Doctor Luis García Barrios</h3>
      <p>García Barrios is a researcher at the Southern Borderland College (ECOSUR) at the San Cristobal de las 
Casas campus. His research is focused one developing and analyzing the theoretical bases of the complex systems, 
its attributes, and its application in the evaluation of alternative production systems, as well as the 
interactive simulation models as for teaching related concepts of dynamic systems, such as its application in 
practical problems in natural resources management.</p>
