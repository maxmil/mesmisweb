      <h2>WHAT IS THE MESMIS PROJECT?</h2>
      <p>The MESMIS Project is a directed effort by professionals and researchers from different areas who are 
interested in the development and utilization of the tools for the evaluation of the sustainability of natural 
resource management systems. The project objectives are focused on the development of the Framework for the 
Evaluation of Natural Resource Management Systems Incorporating Sustainability Indicators (MESMIS) as in its 
application in case studies across the rural sector, mainly in farming contexts of Latin America. Furthermore, it 
is looking to train individuals and organizations interested in evaluating the sustainability and formation of 
human resources through graduate an undergraduate university programs.</p>
      <p>To break with the common problem that the theoretical developments are applied just a few times in 
resolutions for societal problems, the project was designed to cover four main areas: research, training, 
application, and documentation. This, at the same time, gave place to four notable highly integrated action areas: 
</p>
      <h3>1. Research in emergent topics of sustainability</h3>
      <p>where the goal is to contribute information for the  development of concepts and tools for evaluation, as 
well as a critical analysis of the opportunities and challenges that still exist for the operation of the 
sustainability concept. The different thematic areas where it has been deepened are:</p>
      <ul>
        <li>Trend analysis in sustainability evaluation</li>
        <li>Characterization of managing systems</li>
        <li>Sustainability indicators</li>
        <li>Sustainability indicators integration</li>
        <li>Multi-step analysis</li>
        <li>Dynamic systems</li>
        <li>Decision taking</li>
      </ul>
      <h3>2. Application and Systematizing of case studies</h3>
      <p>The systematizing of the experiences that have been generated from the application of the MESMIS 
framework has been one of the principal concerns of the project. The case studies document the methodology 
application in diverse systems, ecological contexts and socioeconomic situations, from the South Pole to the 
Iberian Peninsula. The registered experiences include a small scale of case studies in wine production systems 
from in Argentina and Portugal, diversified production in multiple ecological layers of the Andean zones, and the 
communitarian management of forests and jungles, agrosilvopastorile systems and the small-scale ranching in 
Mexico. </p>
      <h3>3. Human Resources Formation</h3>
      <p>This has been a fundamental area in the MESMIS project that has helped form a wide group of people with a 
new multidimensional approach of natural resource management and its associated ecosystems. It attempts to cover 
diverse educational areas, as in graduate and posgraduate university programs in Mexico and Spain, international 
courses and training workshops to government employees, future technicians and rural promoters. </p>
      <h3>4. Distribution and generation of reference materials</h3>
      <p>The MESMIS project is also looking to generate reference materials in order to create theoretical and 
methodological center concerning the sustainability concept and its evaluation, the objective is to promote its 
discussion and practice. These materials include publications such as peer-reviewed articles, books, reports and 
working documents, as well as the SUSTENTA data base, which includes a group of selected references on 
sustainability and its evaluation. </p>
      <p>For detailed information about the MESMIS project, the research areas and the case studies 
systematization refer to <a 
href="http://www.ciga.unam.mx/ciga/images/stories/publicaciones/sustentabilidad/GIRA_CS3_final.pdf" 
target="blank">Astier et al. 2008</a></p>
