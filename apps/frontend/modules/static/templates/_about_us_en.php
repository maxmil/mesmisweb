      <h2>PARTITIPANT INSTITUTIONS</h2>
      <p>One of the MESMIS project objectives has been the consolidation of a research team, multi-institutional 
and interdisciplinary, where involved institutions and organizations could work together on natural resources 
management. These groups are formed by four academic institutions and one civil organization: The Center of 
Ecosystems Research (CIECO-UNAM), Environmental Geography Research Center (CIGA-UNAM), Research Center in 
Agriculture Sciences (CICA-UAEM), College of the Southern Borderland (ECOSUR) and the Interdisciplinary Group of 
Appropriate Rural Technology (GIRA, A.C.) </p>
      <p>This group collaborates in posgraduate courses in different universities, training workshops, for the 
generation of reference material and documents as well as in national and international publication. Furthermore, 
all members assess research work in graduate and posgraduate research theses, dissertations, and local, national 
and international studies. </p>
      <p>Each member also maintains close relations with institutions in Latin America, Spain, Holland and France, 
mainly in the education field. This leads to the generation of academic exchange as well as evaluations en the 
sustainability evaluation. </p>
      <p>The arrangement of this research team has helped give stronger theoretical bases of sustainability 
evaluation and its application in concrete experiences. Now, it is in a consolidation phase, where the objective 
is to improve the conceptual tools and practices, so they could be use for different parties involved in natural 
resources management. </p>
